    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tblEstoque.getSelectedRow();
            
        Produto obj = new Produto();
        obj.setCodigo(Integer.parseInt(tblEstoque.getValueAt(linhaSelecionada, 0).toString()));
        obj.setDescricao(tblEstoque.getValueAt(linhaSelecionada, 1).toString());
        obj.setCor(tblEstoque.getValueAt(linhaSelecionada, 2).toString());
        obj.setValor(Double.parseDouble(tblEstoque.getValueAt(linhaSelecionada,3).toString()));
        obj.setTamanho(Integer.parseInt(tblEstoque.getValueAt(linhaSelecionada,4).toString()));
        obj.setQuantidade(Integer.parseInt(tblEstoque.getValueAt(linhaSelecionada,5).toString()));
        obj.setFornecedor(tblEstoque.getValueAt(linhaSelecionada, 6).toString());
            
        TelaProduto telaProduto = new TelaProduto(obj);
        telaProduto.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        Validador validador = new Validador();
        validador.validaEntradaPalavra(evt, txtPesquisar, "o código do produto");
        
        try{
            String procurarDescricao = txtPesquisar.getText();
            objProduto = new Produto(procurarDescricao);
            
             ArrayList<Produto> lista = ProdutoDAO.listar(objProduto);
             DefaultTableModel modelo = (DefaultTableModel) tblEstoque.getModel();
             
             modelo.setRowCount(0);
             for(Produto item:lista){
                 modelo.addRow(new String[]{String.valueOf(item.getCodigo()),
                                            String.valueOf(item.getDescricao()),
                                            String.valueOf(item.getCor()),
                                            String.valueOf(item.getValor()),
                                            String.valueOf(item.getTamanho()),
                                            String.valueOf(item.getQuantidade()),
                                            String.valueOf(item.getFornecedor())
                                            });
             }
             
        }catch (SQLException ex) {
            Logger.getLogger(TelaConsultaProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

